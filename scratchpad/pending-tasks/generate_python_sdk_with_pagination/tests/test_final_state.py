import os
import subprocess
import pytest

PROJECT_DIR = "/home/user"
SDK_DIR = "/home/user/sdk"
OPENAPI_FILE = "/home/user/openapi.yaml"

def test_openapi_spec_valid_via_cli():
    """Priority 1: Use Speakeasy CLI to verify the OpenAPI specification is valid."""
    result = subprocess.run(
        ["speakeasy", "validate", "-s", OPENAPI_FILE],
        capture_output=True, text=True, cwd=PROJECT_DIR
    )
    assert result.returncode == 0, f"'speakeasy validate' failed: {result.stderr or result.stdout}"

def test_pagination_extension_exists():
    """Priority 3 fallback: basic file content check for pagination extension."""
    with open(OPENAPI_FILE) as f:
        content = f.read()
    assert "x-speakeasy-pagination:" in content, "Expected 'x-speakeasy-pagination' in openapi.yaml."
    assert "type: offsetLimit" in content, "Expected pagination type to be 'offsetLimit' in openapi.yaml."
    assert "results: $.data" in content, "Expected pagination results mapping to be '$.data' in openapi.yaml."

def test_sdk_directory_and_files_exist():
    """Priority 3 fallback: check if the SDK was generated."""
    assert os.path.isdir(SDK_DIR), f"SDK directory {SDK_DIR} does not exist. Did the generation fail?"
    
    # Check for typical Python SDK files generated by Speakeasy
    setup_exists = os.path.isfile(os.path.join(SDK_DIR, "setup.py"))
    pyproject_exists = os.path.isfile(os.path.join(SDK_DIR, "pyproject.toml"))
    
    assert setup_exists or pyproject_exists, "Could not find setup.py or pyproject.toml in the generated SDK directory. SDK might not be generated correctly."
